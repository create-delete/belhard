package by.belhard.java18.lessons.lesson2;

public class Main {
    public static void main(String[] args) {
        int a = 10;
        System.out.println(a++);
        System.out.println(++a);
        System.out.println(a--);
        System.out.println(--a);
        int a1 = 421321, a2 = 76543;
        int a3 = a1 & a2;
        System.out.println(a3);
        System.out.println(~a3);
    }
}
